<template>
  <div class="app">
    <h1>{{msg}}</h1>
<!--  通过父组件给子组件传递函数类型的props实现  -->
    <School :getSchoolName="getSchoolName"/>

<!--  通过父组件给子组件绑定一个自定义事件（第一种写法，使用@或v-on）  -->
    <Student v-on.once="getStudentName"/>

<!--  通过父组件给子组件绑定一个自定义事件（第二种写法，使用ref）  -->
    <Student ref="student"/>
  </div>
</template>

<script>
import Student from './components/Student'
import School from './components/School'

export default {
  name: 'App',
  components: {Student, School},
  data() {
    return {
      msg: '你好啊'
    }
  },
  methods: {
    getSchoolName(name) {
      console.log('App收到了学校名', name);
    },
    getStudentName(name) {
      console.log('App收到了学生名', name);
    }
  },
  mounted() {
    // this.$refs.student.$on('macabaca', this.getStudentName) # 绑定自定义事件
    // this.$refs.student.$once('macabaca', this.getStudentName) # 绑定自定义事件（一次性）
  }
}
</script>

